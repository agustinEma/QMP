public class Color {
  double rojo;
  double verde;
  double azul;

  public Color(double rojo, double verde, double azul) {
    this.rojo = rojo;
    this.verde = verde;
    this.azul = azul;
  }

  public double getRojo() {
    return  this.rojo;
  }

  public double getVerde() {
    return  this.verde;
  }

  public double getAzul() {
    return  this.azul;
  }
}
